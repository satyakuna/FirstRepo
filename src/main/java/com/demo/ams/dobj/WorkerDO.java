package com.demo.ams.dobj;

public class WorkerDO {
	
	private int workerID;
	private String workerName;
	private String workerContactNo;
	private String workerAltContactNo;
	private String workerAddr;
	private String workerProofID;
	private String workerProofType;
	private String workerJobDesc;
	
	public String getWorkerJobDesc() {
		return workerJobDesc;
	}
	public void setWorkerJobDesc(String workerJobDesc) {
		this.workerJobDesc = workerJobDesc;
	}
	public int getWorkerID() {
		return workerID;
	}
	public void setWorkerID(int workerID) {
		this.workerID = workerID;
	}
	public String getWorkerName() {
		return workerName;
	}
	public void setWorkerName(String workerName) {
		this.workerName = workerName;
	}
	public String getWorkerContactNo() {
		return workerContactNo;
	}
	public void setWorkerContactNo(String workerContactNo) {
		this.workerContactNo = workerContactNo;
	}
	public String getWorkerAltContactNo() {
		return workerAltContactNo;
	}
	public void setWorkerAltContactNo(String workerAltContactNo) {
		this.workerAltContactNo = workerAltContactNo;
	}
	public String getWorkerAddr() {
		return workerAddr;
	}
	public void setWorkerAddr(String workerAddr) {
		this.workerAddr = workerAddr;
	}
	public String getWorkerProofID() {
		return workerProofID;
	}
	public void setWorkerProofID(String workerProofID) {
		this.workerProofID = workerProofID;
	}
	public String getWorkerProofType() {
		return workerProofType;
	}
	public void setWorkerProofType(String workerProofType) {
		this.workerProofType = workerProofType;
	}
}
